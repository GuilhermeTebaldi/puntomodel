export const resolveLanguageFromCountry = (countryCode?: string | null): LanguageCode | null => {
  if (!countryCode) return null;
  const code = countryCode.trim().toLowerCase();
  return countryLanguageMap[code] ?? null;
};

type LabeledOption = {
  id: string;
  labels: Record<LanguageCode, string>;
  aliases?: string[];
};

const normalize = (value: string) => value.trim().toLowerCase();

const matchLabeledOption = (value: string, options: readonly LabeledOption[]) => {
  const normalized = normalize(value);
  return options.find((option) => {
    if (normalize(option.id) === normalized) return true;
    if (option.aliases?.some((alias) => normalize(alias) === normalized)) return true;
    return Object.values(option.labels).some((label) => normalize(label) === normalized);
  });
};

