type TranslationNode = string | ReadonlyArray<TranslationNode> | { [key: string]: TranslationNode };

const resolvePath = (node: TranslationNode, path: string): TranslationNode | undefined => {
  return path.split('.').reduce<TranslationNode | undefined>((acc, key) => {
    if (!acc || typeof acc === 'string' || Array.isArray(acc)) return undefined;
    return acc[key];
  }, node);
};

const interpolate = (value: string, params?: Record<string, string | number>) => {
  if (!params) return value;
  return value.replace(/\{\{\s*(\w+)\s*\}\}/g, (_, key) => {
    const replacement = params[key];
    return replacement === undefined ? '' : String(replacement);
  });
};

export const getTranslation = (
  lang: LanguageCode,
  key: string,
  params?: Record<string, string | number>
) => {
  const value =
    resolvePath(translations[lang], key) ?? resolvePath(translations[fallbackLanguage], key);
  if (typeof value === 'string') return interpolate(value, params);
  return key;
};

export const getList = (lang: LanguageCode, key: string) => {
  const value =
    resolvePath(translations[lang], key) ?? resolvePath(translations[fallbackLanguage], key);
  return Array.isArray(value) ? (value as string[]) : [];
};

export const translateErrorMessage = (message: string, lang: LanguageCode) => {
  const key = errorMessageKeyMap[message];
  if (!key) return message;
  return getTranslation(lang, key);
};
