export type PriceId = (typeof priceOptions)[number]['id'];

export const getPriceId = (label: string): PriceId | null => {
  const match = matchLabeledOption(label, priceOptions);
  return match?.id as PriceId | null;
};

export const getPriceLabel = (id: PriceId, lang: LanguageCode) => {
  const option = priceOptions.find((item) => item.id === id);
  return option?.labels[lang] ?? option?.labels[fallbackLanguage] ?? id;
};

